// ===== TYPING EFFECT STRINGS =====
const typingStrings = {
  en: ["Full Stack Developer", "UI/UX Designer", "Frontend Specialist", "Freelance Developer"],
  pt: ["Desenvolvedor Full Stack", "Designer UI/UX", "Especialista Frontend", "Desenvolvedor Freelance"],
};

// ===== TRANSLATE PAGE =====
function translatePage(lang) {
  const elements = document.querySelectorAll("[data-translate-key]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-translate-key");
    const value = translations[lang] && translations[lang][key];
    if (!value) return;

    if (el.placeholder !== undefined && el.tagName !== "BUTTON" && el.tagName !== "A") {
      el.placeholder = value;
    } else if (el.tagName === "INPUT" && el.type === "submit") {
      el.value = value;
    } else {
      el.innerHTML = value;
    }
  });
}

// ===== LANGUAGE TOGGLE =====
document.addEventListener("DOMContentLoaded", () => {
  const btn = document.getElementById("translate-btn");
  const langCode = document.querySelector(".lang-text");
  const flagImage = document.getElementById("flag-image");

  let currentLang = localStorage.getItem("lang") || "en";
  document.documentElement.lang = currentLang;
  translatePage(currentLang);

  langCode.textContent = translations[currentLang]["translate-button"];
  flagImage.src = flagImages[currentLang];
  flagImage.alt = currentLang === "pt" ? "Bandeira do Brasil" : "United States Flag";
  btn.dataset.lang = currentLang;

  btn.addEventListener("click", () => {
    const cur = btn.dataset.lang;
    const next = cur === "pt" ? "en" : "pt";

    btn.dataset.lang = next;
    langCode.textContent = translations[next]["translate-button"];
    flagImage.src = flagImages[next];
    flagImage.alt = next === "pt" ? "Bandeira do Brasil" : "United States Flag";
    document.documentElement.lang = next;
    localStorage.setItem("lang", next);
    translatePage(next);

    // Restart typing with new language
    restartTyping();
  });
});

// ===== TYPING EFFECT =====
let typingIndex = 0;
let charIndex = 0;
let isDeleting = false;
let typingTimer = null;

function typeText() {
  const lang = document.documentElement.lang || "en";
  const strings = typingStrings[lang] || typingStrings.en;
  const current = strings[typingIndex % strings.length];
  const el = document.getElementById("typing-text");
  if (!el) return;

  if (isDeleting) {
    el.textContent = current.substring(0, charIndex - 1);
    charIndex--;
  } else {
    el.textContent = current.substring(0, charIndex + 1);
    charIndex++;
  }

  let delay = isDeleting ? 40 : 90;

  if (!isDeleting && charIndex === current.length) {
    delay = 2200;
    isDeleting = true;
  } else if (isDeleting && charIndex === 0) {
    isDeleting = false;
    typingIndex = (typingIndex + 1) % strings.length;
    delay = 350;
  }

  typingTimer = setTimeout(typeText, delay);
}

function restartTyping() {
  clearTimeout(typingTimer);
  charIndex = 0;
  isDeleting = false;
  const el = document.getElementById("typing-text");
  if (el) el.textContent = "";
  typeText();
}

document.addEventListener("DOMContentLoaded", () => {
  setTimeout(typeText, 600);
});

// ===== SCROLL REVEAL =====
document.addEventListener("DOMContentLoaded", () => {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
        }
      });
    },
    { threshold: 0.1, rootMargin: "0px 0px -40px 0px" }
  );

  document.querySelectorAll(".reveal").forEach((el) => observer.observe(el));
});

// ===== ACTIVE NAV ON SCROLL =====
document.addEventListener("DOMContentLoaded", () => {
  const sections = document.querySelectorAll("section[id]");
  const navLinks = document.querySelectorAll(".navbar a");

  function updateActiveLink() {
    let current = "";
    sections.forEach((section) => {
      const top = section.offsetTop - 120;
      if (window.scrollY >= top) {
        current = section.getAttribute("id");
      }
    });

    navLinks.forEach((link) => {
      link.classList.remove("active");
      if (link.getAttribute("href") === `#${current}`) {
        link.classList.add("active");
      }
    });
  }

  window.addEventListener("scroll", updateActiveLink, { passive: true });
  updateActiveLink();
});

// ===== MOBILE MENU =====
document.addEventListener("DOMContentLoaded", () => {
  const menuIcon = document.querySelector("#menu-icon");
  const navbar = document.querySelector(".navbar");

  if (!menuIcon || !navbar) return;

  const toggleMenu = () => {
    const isOpen = navbar.classList.toggle("active");
    menuIcon.classList.toggle("bx-x", isOpen);
    menuIcon.setAttribute("aria-expanded", String(isOpen));
  };

  const closeMenu = () => {
    menuIcon.classList.remove("bx-x");
    navbar.classList.remove("active");
    menuIcon.setAttribute("aria-expanded", "false");
  };

  menuIcon.addEventListener("click", toggleMenu);

  // Keyboard support (Enter / Space)
  menuIcon.addEventListener("keydown", (e) => {
    if (e.key === "Enter" || e.key === " ") {
      e.preventDefault();
      toggleMenu();
    }
  });

  // Close on nav link click
  navbar.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMenu);
  });
});

// ===== COPY EMAIL =====
document.addEventListener("DOMContentLoaded", () => {
  const copyButtons = document.querySelectorAll(".copy-email-btn");
  const tooltips = document.querySelectorAll(".email-copied-tooltip");
  const emailToCopy = "raphael.moreira@soulasalle.com.br";

  copyButtons.forEach((btn, i) => {
    btn.addEventListener("click", (e) => {
      e.preventDefault();
      navigator.clipboard
        .writeText(emailToCopy)
        .then(() => {
          tooltips[i].classList.add("show");
          setTimeout(() => tooltips[i].classList.remove("show"), 2500);
        })
        .catch(() => {
          alert("Não foi possível copiar o email.");
        });
    });
  });
});
